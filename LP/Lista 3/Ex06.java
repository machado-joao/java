public class Ex06 {
 
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println(a / b);
        System.out.println((float) a / b);
        System.out.println((float) a / (float) b);
        System.out.println(a / (float) b);
        System.out.println((float) (a / b));
    }

}
